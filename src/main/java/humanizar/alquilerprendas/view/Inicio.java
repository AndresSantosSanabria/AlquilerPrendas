    private void BtnPorAlquilerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnPorAlquilerMouseClicked
        // Solicitar el n√∫mero de alquiler al usuario
        String input = NAlquiler.getText();

        // Convertir el input a entero
        try {
            Integer idServicio = Integer.parseInt(input.trim());

            // Crear instancia del controlador
            AlquilerController controller = new AlquilerController();

            // Mostrar los resultados en la tabla
            controller.mostrarAlquilerEnTabla(idServicio, jtPrendas);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "Por favor, ingrese un n√∫mero v√°lido.",
                    "Error de formato",
                    JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error al buscar el alquiler: " + e.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_BtnPorAlquilerMouseClicked

