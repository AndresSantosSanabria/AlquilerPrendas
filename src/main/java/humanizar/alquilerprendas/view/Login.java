    private void BtnIngresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnIngresarMouseClicked
        if (jUsuario != null && !jUsuario.getText().isEmpty()
                && jPassword != null && !jPassword.getText().isEmpty()) {
            
            int validation = login.login(jUsuario.getText(), jPassword.getText());

            if (validation > 0) {
                
               login.verificarRolYRedirigir(validation);
               
               this.dispose();
               /*System.out.println(validation);
               home h = new home(validation);
               h.setVisible(true);
               */
            } else {
                JOptionPane.showMessageDialog(null, "El Usuario y la contrase√±a ingresado es incorrecto", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Los campos de usuario y contrase√±a no pueden estar vac√≠os", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_BtnIngresarMouseClicked

